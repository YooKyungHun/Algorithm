# 15, 25, 35, 45, 55, 555 에 대해 적용가능 해야함
def solution(storey):
    answer = 0

    while storey > 0:
        if storey % 10 > 5:
            answer += 10 - (storey % 10)
            storey = storey // 10 + 1

        elif storey % 10 == 5:
            if (storey // 10) % 10 >= 5:
                answer += 5
                storey = storey // 10 + 1
            else:
                answer += 5
                storey = storey // 10

        elif storey % 10 < 5:
            answer += storey % 10
            storey = storey // 10

    return answer
