if H > N:
    H, N = N, H

dp = [[0] * 31 for _ in range(31)]

for i in range(31):
    for j in range(31):
        if j == 0:
            dp[i][j] = 1
        elif i >= j:
            dp[i][j] = dp[i-1][j] + dp[i][j-1]

# print(dp[N-H][N-H])
print(dp)

